#include<iostream>
using namespace std;
int main(){
    int a=5;
    int b=3;
    // airthmetic operator 
    cout<<a+b<<endl;
    cout<<a-b<<endl;
    cout<<a*b<<endl;
    cout<<a/b<<endl;
    cout<<a%b<<endl;

    //relational operator
    cout<<(a>b)<<endl;
    cout<<(a<b)<<endl;
    cout<<(a<=b)<<endl;
    cout<<(a>=b)<<endl;
    cout<<(a!=b)<<endl;
    cout<<(a==b)<<endl;

    //assignment operator
    int a=5;
    cout<<a<<endl;

    //logical operator
    // &&-- both condition true 
    // || -- one condition can be true 
    // ! -- not 
}